# Logical Operators
print(True and True)
print(True and False)
print(False and True)
print(False and False)
print()

print(True or True)
print(True or False)
print(False or True)
print(False or False)
print()

print(not True)
print(not False)
print()

# Comparison Operators
print(10 == 5)
print(10 != 5)
print(10 < 5)
print(10 <= 5)
print(10 > 5)
print(10 >= 5)

# Arithmetic Operators
print(10+5)
print(10-5)
print(10*5)
print(10/5)
print(10//4)
print(10 % 4)
print(10**5)
